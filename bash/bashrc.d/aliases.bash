# Print a terminal bell
alias bell='printf %s '$'\a'

# Don't print the bc(1) welcome message
alias bc='bc -q'

# Use a unified format for diff(1) by default
alias diff='diff -u'

# Don't print the GDB copyright message on every invocation
alias gdb='gdb -q'


# sudo(8) should always attempt to use the target user's $HOME
alias sudo='sudo -H'

