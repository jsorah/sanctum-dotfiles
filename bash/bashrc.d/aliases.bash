# Print a terminal bell
alias bell='printf '$'\a'

# Use a unified format for diff(1) by default
alias diff='diff -u'

# Don't print the GDB copyright message on every invocation
alias gdb='gdb -q'

# Use a unified format for rcsdiff(1) by default
alias rcsdiff='rcsdiff -u'

